package com.trangiabao.giaothong.tracuu.luat.model;

public class NoiDung {

    private int id;
    private String noiDung;

    public NoiDung(int id, String noiDung) {
        this.id = id;
        this.noiDung = noiDung;
    }

    public int getId() {
        return id;
    }

    public String getNoiDung() {
        return noiDung;
    }
}
